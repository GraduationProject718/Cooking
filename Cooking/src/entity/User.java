package entity;


public class User {
	private int u_id;
	private String u_account;
	private String u_password;
	private String u_nickname;
	private String u_phone;
	private String u_email;
	private String u_information;
	private String u_imgName;
	private String u_imgUrl;
	public int getU_id() {
		return u_id;
	}
	public void setU_id(int u_id) {
		this.u_id = u_id;
	}
	public String getU_account() {
		return u_account;
	}
	public void setU_account(String u_account) {
		this.u_account = u_account;
	}
	public String getU_password() {
		return u_password;
	}
	public void setU_password(String u_password) {
		this.u_password = u_password;
	}
	public String getU_nickname() {
		return u_nickname;
	}
	public void setU_nickname(String u_nickname) {
		this.u_nickname = u_nickname;
	}
	public String getU_phone() {
		return u_phone;
	}
	public void setU_phone(String u_phone) {
		this.u_phone = u_phone;
	}
	public String getU_email() {
		return u_email;
	}
	public void setU_email(String u_email) {
		this.u_email = u_email;
	}
	public String getU_information() {
		return u_information;
	}
	public void setU_information(String u_information) {
		this.u_information = u_information;
	}
	public String getU_imgName() {
		return u_imgName;
	}
	public void setU_imgName(String u_imgName) {
		this.u_imgName = u_imgName;
	}
	public String getU_imgUrl() {
		return u_imgUrl;
	}
	public void setU_imgUrl(String u_imgUrl) {
		this.u_imgUrl = u_imgUrl;
	}
	
}
